/**
 * ID 기반으로 summary 필드를 교체.
 * title 필드에 curly-quote 포함돼도 안전: [^']는 U+0027만 제외.
 */
function fix(id, newSummary) {
  const re = new RegExp(`(id: '${id}',[\\s\\S]*?summary: ')[^']*'`);
  const before = src;
  src = src.replace(re, `$1${newSummary}'`);
  if (src !== before) {
    fixed++;
    console.log(`✅ ${id}: ${newSummary.slice(0, 65)}`);
  } else {
    console.log(`❌ 실패: ${id}`);
  }
}

